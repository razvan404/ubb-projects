<h3>Ingrediente</h3>
<p>
● 2 plicuri zahar vanilat bourbon<br>
● 5 oua mici<br>
● 200g faina<br>
● 200g zahar<br>
● 1 lingurita paf de copt<br>
● 2 linguri cacao<br>
● 4 linguri ulei<br>
● 1 praf de sare<br>
● pentru ornat cateva alune de padure<br>
</p>
<hr>
<h3>Mod de preparare</h3>
<p>
   Separam albusurile de galbenusuri si batem albusurile cu un praf de sare, cu ajutorul mixerului. Cand albusurile au devenit spumoase adaugam zaharul vanilat bourbon si zaharul tos pana cand obtinem o spuma tare si lucioasa.<br>
   Galbenusurile se amesteca cu uleiul ca la maioneza, se toarna cate putin, si se pun peste albusurile batute spuma amestecand de jos in sus cu o spatula.<br>
   Se amesteca faina cu praful de copt si se pune peste amestecul de albusuri si galbenusuri lingura cu lingura amestecan tot cu spatula cu miscari largi de jos in sus ptr a nu scoate aerul din albusuri. Impartim compozitia formata in doua si intr-una din ele punem cacaoa pe care am amestecat-o bine cu 2-3 linguri de apa calda.<br>
   Intr-o tava mare de cozonac tapetata cu hartie de copt (eu am folosit forme cumparate din hartie) incepem sa formam checul. Punem in tava cu o lingura din compozitia alba si peste din compozitia cu cacao, se procedeaza la fel pana terminam ambele comozitii. Vom obtine un chec pufos marmorat. Dam checul la cuptor la aragazul incins inainte la 180°C pentru 30-40 minute pana ce checul e bine copt, cand e pe jumatate copt am pus peste el cateva alune de padure.<br>
   La sfarsit se face testul cu scobitoarea. Se poate servi cu cafea, lapte dulce sau batut.<br>
</p>
